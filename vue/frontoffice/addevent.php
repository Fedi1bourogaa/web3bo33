<?php
require_once '../../controller/EventE.php';
require_once '../../model/Event.php';

ini_set('display_errors', 1);
ini_set('display_startup_errors', 1);
error_reporting(E_ALL);

// Vérification si le formulaire est soumis
if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    // Vérification des champs requis
    if (
        isset($_POST["event_name"]) &&
        isset($_POST["event_date"]) &&
        isset($_POST["event_location"]) &&
        isset($_FILES["fileToUpload"])
    ) {
        $target_dir = "../../uploads/";
        $target_file = $target_dir . basename($_FILES["fileToUpload"]["name"]);
        $uploadOk = true;
        $imageFileType = strtolower(pathinfo($target_file, PATHINFO_EXTENSION));

        // Validation de l'image
        $check = getimagesize($_FILES["fileToUpload"]["tmp_name"]);
        if ($check === false) {
            echo "Le fichier n'est pas une image.";
            $uploadOk = false;
        }

        // Vérification de la taille du fichier
        if ($_FILES["fileToUpload"]["size"] > 500000) {
            echo "Désolé, votre fichier est trop volumineux.";
            $uploadOk = false;
        }

        // Autoriser uniquement certains formats
        $allowed_types = ["jpg", "png", "jpeg", "gif"];
        if (!in_array($imageFileType, $allowed_types)) {
            echo "Seuls les formats JPG, JPEG, PNG et GIF sont autorisés.";
            $uploadOk = false;
        }

        if ($uploadOk) {
            // Déplacement du fichier uploadé
            if (move_uploaded_file($_FILES["fileToUpload"]["tmp_name"], $target_file)) {
                // Créer un objet Event
                $event = new Event(
                    0,
                    $_POST['event_name'],
                    $_POST['event_date'],
                    $_POST['event_location'],
                    $target_file // Lien de l'image téléchargée
                );

                // Ajouter l'événement via le contrôleur
                $eventE = new EventE();
                if ($eventE->addEvent($event)) {
                    // Redirection en cas de succès
                    header('Location: listevents.php');
                    exit;
                } else {
                    echo "Erreur lors de l'ajout de l'événement.";
                }
            } else {
                echo "Désolé, une erreur est survenue lors du téléchargement de votre fichier.";
            }
        }
    } else {
        echo "Tous les champs sont requis.";
    }
}

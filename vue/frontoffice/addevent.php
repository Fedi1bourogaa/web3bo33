        function validateForm() {
            let valid = true;

            // Validation du nom de l'événement
            const eventName = document.getElementById("event_name").value;
            const eventNameError = document.getElementById("event_name_error");
            const eventNameSuccess = document.getElementById("event_name_success");
            const eventNameRegex = /^[a-zA-Z\s]+$/; // Vérifie que le nom est composé de lettres et espaces

            if (!eventName || !eventNameRegex.test(eventName)) {
                eventNameError.style.display = "block";
                eventNameSuccess.style.display = "none";
                valid = false;
            } else {
                eventNameError.style.display = "none";
                eventNameSuccess.style.display = "block";
            }

            // Validation de la date de l'événement
            const eventDate = document.getElementById("event_date").value;
            const eventDateError = document.getElementById("event_date_error");
            const eventDateSuccess = document.getElementById("event_date_success");

            if (!eventDate) {
                eventDateError.style.display = "block";
                eventDateSuccess.style.display = "none";
                valid = false;
            } else {
                eventDateError.style.display = "none";
                eventDateSuccess.style.display = "block";
            }

            // Validation du type
            const eventLocation = document.getElementById("event_location").value;
            const eventLocationError = document.getElementById("event_location_error");
            const eventLocationSuccess = document.getElementById("event_location_success");
            const eventLocationRegex = /^[a-zA-Z\s]+$/; // Vérifie que le type est composé de lettres et espaces

            if (!eventLocation || !eventLocationRegex.test(eventLocation)) {
                eventLocationError.style.display = "block";
                eventLocationSuccess.style.display = "none";
                valid = false;
            } else {
                eventLocationError.style.display = "none";
                eventLocationSuccess.style.display = "block";
            }

            // Validation de l'image
            const fileInput = document.getElementById("fileToUpload");
            const fileError = document.getElementById("file_error");
            const fileSuccess = document.getElementById("file_success");

            if (fileInput.files.length === 0) {
                fileError.style.display = "block";
                fileSuccess.style.display = "none";
                valid = false;
            } else {
                fileError.style.display = "none";
                fileSuccess.style.display = "block";
            }

            return valid;
        }
